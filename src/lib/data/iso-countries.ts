/** Reverse lookup: numeric (e.g. "840") → alpha-2 (e.g. "US"). */
export const NUMERIC_TO_ALPHA2: Record<string, string> = Object.fromEntries(
  Object.entries(ISO_COUNTRIES).map(([alpha2, c]) => [c.numeric, alpha2]),
);

export function countryName(alpha2: string | null | undefined): string {
  if (!alpha2) return "Unknown";
  return ISO_COUNTRIES[alpha2.toUpperCase()]?.name ?? alpha2.toUpperCase();
}

/**
 * Render a country flag from its alpha-2 code by mapping each ASCII letter
 * to its Regional Indicator Symbol code point. Returns an empty string for
 * unknown / malformed codes so the UI can fall back to the alpha-2 text.
 */
export function flagEmoji(alpha2: string | null | undefined): string {
  if (!alpha2 || alpha2.length !== 2) return "";
  const upper = alpha2.toUpperCase();
  if (!/^[A-Z]{2}$/.test(upper)) return "";
  const A = 0x1f1e6;
  const a = "A".charCodeAt(0);
  return String.fromCodePoint(
    A + (upper.charCodeAt(0) - a),
    A + (upper.charCodeAt(1) - a),
  );
}
