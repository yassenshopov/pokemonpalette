/**
 * ISO 3166-1 country lookup keyed by alpha-2 code.
 *
 * Used by the admin "Insights" world-map card to:
 *   - resolve the alpha-2 code we store in `users.country_code` to a
 *     human-readable display name, and
 *   - join with the `world-atlas` TopoJSON whose feature `id` field is the
 *     numeric ISO 3166-1 code (zero-padded to 3 digits).
 *
 * Data is pinned: ISO codes basically don't change. Names match the
 * "common" form used by the M49/world-atlas dataset so the alpha-2
 * lookup and the topojson `properties.name` line up for tooltips.
 *
 * The numeric values are *strings* on purpose — world-atlas stores them as
 * "840", "076", etc. and direct equality with the topojson id is the
 * cheapest join.
 */

export interface IsoCountry {
  /** Common short name. */
  name: string;
  /** ISO 3166-1 numeric (zero-padded, string for direct topojson join). */
  numeric: string;
}

